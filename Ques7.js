
// Array 
// 1. Linear Collection 
// 2. Create the Using [] ;
// 3. Array are immutable

// Method 

let arr = [1, 2, 3, 4, 5] ; 

// add the end
arr.push(5);
console.log(arr);

// remove the last
arr.pop();
console.log(arr);

// add the beginning
arr.unshift(0);
console.log(arr);

// delete the first 
arr.shift();
console.log(arr);